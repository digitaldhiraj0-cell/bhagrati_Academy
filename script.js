function t(key) {
  return translations[language][key] || translations.en[key] || key;
}

function getSavedSiteContent() {
  try {
    return JSON.parse(localStorage.getItem(SITE_CONTENT_KEY)) || {};
  } catch (_error) {
    return {};
  }
}

function readSavedCollection(key, fallback) {
  try {
    const value = JSON.parse(localStorage.getItem(key));
    return Array.isArray(value) && value.length ? value : fallback;
  } catch (_error) {
    return fallback;
  }
}

function loadManagedPublicData() {
  teachers = readSavedCollection(TEACHERS_KEY, teachers);
  achievements = readSavedCollection(SHOWCASE_KEY, achievements);
}

function applySavedSiteContent() {
  if (language !== "en") return;

  const content = getSavedSiteContent();
  Object.entries(content).forEach(([key, value]) => {
    document.querySelectorAll(`[data-i18n="${key}"]`).forEach((node) => {
      node.textContent = value;
    });
    document.querySelectorAll(`[data-site-content="${key}"]`).forEach((node) => {
      node.textContent = value;
    });
  });
}

function applyLanguage(nextLanguage) {
  language = nextLanguage;
  document.documentElement.lang = language === "np" ? "ne" : "en";
  document.body.classList.toggle("np", language === "np");

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    node.textContent = t(key);
  });

  document.querySelectorAll("[data-site-content]").forEach((node) => {
    const key = node.dataset.siteContent;
    node.textContent = getSavedSiteContent()[key] || node.textContent;
  });

  applySavedSiteContent();

  document.querySelectorAll(".lang-switch span").forEach((span, index) => {
    span.classList.toggle("active", (language === "en" && index === 0) || (language === "np" && index === 1));
  });

  document.querySelectorAll(".class-tabs button").forEach((button) => {
    const classNumber = Number(button.dataset.classNumber);
    button.textContent = language === "np" ? `कक्षा ${classNumber}` : `Class ${classNumber}`;
  });

  renderClass(activeClass);
}

let activeClass = 1;

function renderClass(classNumber) {
  activeClass = classNumber;
  document.querySelectorAll(".class-tabs button").forEach((button) => {
    button.classList.toggle("active", Number(button.dataset.classNumber) === activeClass);
  });

  document.querySelector("#classTitle").textContent =
    language === "np" ? `कक्षा ${activeClass}` : `Class ${activeClass}`;
  document.querySelector("#classDescription").textContent = t("classDescription");
  const currentResource = classResources.find((item) => item.classNumber === activeClass);
  const pdfLink = document.querySelector("#pdfLink");
  const uploadStatus = document.querySelector(".upload-status");

  if (currentResource?.pdfUrl) {
    pdfLink.href = currentResource.pdfUrl;
    pdfLink.hidden = false;
    pdfLink.textContent = currentResource.pdfOriginalName || t("viewPdf");
  } else {
    pdfLink.hidden = true;
    pdfLink.removeAttribute("href");
  }

  if (uploadStatus && !uploadStatus.dataset.keep) {
    uploadStatus.textContent = currentResource?.pdfUrl ? "" : t("noPdf");
  }

  const grid = document.querySelector("#subjectGrid");
  grid.innerHTML = subjects
    .map(
      (subject, index) => `
        <article class="subject-card">
          <strong>${language === "np" ? translateSubject(subject) : subject}</strong>
          <span>${language === "np" ? "शिक्षक" : "Teacher"}: ${teachers[index % teachers.length].name}</span>
          <span>${language === "np" ? "साप्ताहिक रुटिन र गृहकार्य तयार" : "Routine and assignments ready"}</span>
        </article>
      `,
    )
    .join("");
}

async function loadClassResources() {
  try {
    const response = await fetch(`${API_BASE}/api/classes`);
    if (!response.ok) return;
    classResources = await response.json();
    renderClass(activeClass);
  } catch (_error) {
    classResources = [];
  }
}

function translateSubject(subject) {
  const subjectMap = {
    English: "अंग्रेजी",
    Nepali: "नेपाली",
    Mathematics: "गणित",
    Science: "विज्ञान",
    "Social Studies": "सामाजिक अध्ययन",
    Computer: "कम्प्युटर",
  };
  return subjectMap[subject] || subject;
}

function setupClassTabs() {
  const tabs = document.querySelector(".class-tabs");
  tabs.innerHTML = Array.from({ length: 10 }, (_, index) => {
    const classNumber = index + 1;
    return `<button type="button" data-class-number="${classNumber}">Class ${classNumber}</button>`;
  }).join("");

  tabs.addEventListener("click", (event) => {
    const button = event.target.closest("button");
    if (!button) return;
    renderClass(Number(button.dataset.classNumber));
  });
}

