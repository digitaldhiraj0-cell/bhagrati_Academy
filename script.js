function setupContactForm() {
  const form = document.querySelector("#contactForm");
  const status = document.querySelector(".form-status");

  form.addEventListener("submit", (event) => {
    event.preventDefault();
    const data = Object.fromEntries(new FormData(form).entries());
    const messages = JSON.parse(localStorage.getItem("bhagirathiContactMessages") || "[]");
    messages.push({ ...data, createdAt: new Date().toISOString() });
    localStorage.setItem("bhagirathiContactMessages", JSON.stringify(messages));

    const body = encodeURIComponent(
      `Name: ${data.name}\nEmail: ${data.email}\nSubject: ${data.subject}\n\n${data.message}`,
    );
    window.location.href = `mailto:info@bhagirathiacademy.edu.np?subject=${encodeURIComponent(data.subject)}&body=${body}`;
    status.textContent = t("formSuccess");
    form.reset();
  });
}

function setupLoginModal() {
  const modal = document.querySelector("#loginModal");
  const loginRole = document.querySelector("#loginRole");
  const loginForm = document.querySelector("#loginForm");
  const loginStatus = document.querySelector(".login-status");
  let selectedRole = "student";

  document.querySelectorAll(".open-login").forEach((button) => {
    button.addEventListener("click", () => {
      selectedRole = button.dataset.role.toLowerCase();
      loginRole.textContent = `${button.dataset.role} Portal`;
      loginStatus.textContent = "";
      modal.showModal();
    });
  });

  document.querySelector(".close-modal").addEventListener("click", () => {
    modal.close();
  });

  loginForm.addEventListener("submit", async (event) => {
    event.preventDefault();
    loginStatus.textContent = "";
    const formData = new FormData(loginForm);
    const username = String(formData.get("username") || "").trim();
    const password = String(formData.get("password") || "");

    const completeDemoLogin = (account) => {
      const result = {
        token: `demo-token-${selectedRole}`,
        user: {
          username: account.username,
          fullName: account.fullName,
          role: selectedRole,
        },
      };

      authState = result;
      localStorage.setItem("bhagirathiAuth", JSON.stringify(result));
      loginStatus.textContent = `${t("loginSuccess")} ${result.user.fullName}.`;
      alert("Login Successful!");
      setTimeout(() => modal.close(), 500);
    };

    const demoAccount = demoAccounts[selectedRole];
    const credentialsMatch =
      demoAccount && username === demoAccount.username && password === demoAccount.password;

    if (window.location.protocol === "file:") {
      if (credentialsMatch) {
        completeDemoLogin(demoAccount);
      } else {
        loginStatus.textContent = "Invalid Username or Password";
      }
      return;
    }

    try {
      const response = await fetch(`${API_BASE}/api/auth/login`, {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({
          username,
          password,
          role: selectedRole,
        }),
      });
      const result = await response.json();

      if (!response.ok) {
        throw new Error(result.message || t("loginFailed"));
      }

      authState = result;
      localStorage.setItem("bhagirathiAuth", JSON.stringify(result));
      loginStatus.textContent = `${t("loginSuccess")} ${result.user.fullName}.`;
      setTimeout(() => modal.close(), 700);
    } catch (error) {
      if (credentialsMatch) {
        completeDemoLogin(demoAccount);
        return;
      }

      loginStatus.textContent = "Invalid Username or Password";
    }
  });
}

function setupPdfUpload() {
  const form = document.querySelector("#pdfUploadForm");
  const status = document.querySelector(".upload-status");

  form.addEventListener("submit", async (event) => {
    event.preventDefault();
    status.dataset.keep = "true";
    status.textContent = "";

    if (!authState?.token || authState.user.role !== "admin") {
      status.textContent = t("uploadLoginRequired");
      return;
    }

    const fileInput = document.querySelector("#classPdf");
    if (!fileInput.files.length) return;

    const payload = new FormData();
    payload.append("pdf", fileInput.files[0]);

    try {
      const response = await fetch(`${API_BASE}/api/classes/${activeClass}/pdf`, {
        method: "POST",
        headers: { Authorization: `Bearer ${authState.token}` },
        body: payload,
      });
      const result = await response.json();

      if (!response.ok) {
        throw new Error(result.message || "Upload failed.");
      }

      const existingIndex = classResources.findIndex((item) => item.classNumber === result.classNumber);
      if (existingIndex >= 0) {
        classResources[existingIndex] = result;
      } else {
        classResources.push(result);
      }

      status.textContent = t("uploadSuccess");
      fileInput.value = "";
      renderClass(activeClass);
      status.dataset.keep = "true";
      status.textContent = t("uploadSuccess");
    } catch (error) {
      status.textContent = error.message || "Upload failed.";
    }
  });
}

